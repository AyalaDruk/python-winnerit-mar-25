# String sorting - by ASCII

words = ["AB", "aB", "ab", "BC", "Zz", "ZZ"]
print(f"{words = }")
print(id(words))

words.sort()
print(f"{words = }")
print(id(words))

words.sort(reverse=True)
print(f"{words = }")
print(id(words))